public class MathRobot {

//Overload
 int addition(int a, int b){
    int c =  a + b;
    return c;
 }
 
 int addition(int a, int b, int c){
    int d =  a + b + c;
    return d;
 }
 double addition(double a, double b){
    double c =  a + b;
    return c;
 }
 
 double addition(double a, double b, double c){
    double d =  a + b + c;
    return d;
 }

}
